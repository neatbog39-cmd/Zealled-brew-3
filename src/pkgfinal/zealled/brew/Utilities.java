    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         if (editingUserId == -1) return;
        
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText();
        String confirmPassword = txtConfirmPassword.getText();  // ← NEW
        String fullName = txtFullName.getText().trim();
        String role = cmbRole.getSelectedItem().toString();
        
        if (username.isEmpty() || fullName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and Full Name required!");
            return;
        }
        
        // Optional password change validation
        if (!password.isEmpty()) {
            if (confirmPassword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill Confirm Password!");
                return;
            }
            if (!password.equals(confirmPassword)) {
                JOptionPane.showMessageDialog(this, "Passwords do not match!");
                txtConfirmPassword.setText("");
                txtConfirmPassword.requestFocus();
                return;
            }
        }
        
        if (isUsernameExists(username, editingUserId)) {
            JOptionPane.showMessageDialog(this, "Username '" + username + "' already exists!");
            return;
        }
        
        try (Connection con = ConnectorXampp.connect()) {
            if (!password.isEmpty()) {
                // Update with NEW password
                String sql = "UPDATE users SET username = ?, password = MD5(?), role = ?, full_name = ? WHERE id = ?";
                try (PreparedStatement pst = con.prepareStatement(sql)) {
                    pst.setString(1, username);
                    pst.setString(2, password);
                    pst.setString(3, role);
                    pst.setString(4, fullName);
                    pst.setInt(5, editingUserId);
                    pst.executeUpdate();
                }
            } else {
                // Update WITHOUT password change
                String sql = "UPDATE users SET username = ?, role = ?, full_name = ? WHERE id = ?";
                try (PreparedStatement pst = con.prepareStatement(sql)) {
                    pst.setString(1, username);
                    pst.setString(2, role);
                    pst.setString(3, fullName);
                    pst.setInt(4, editingUserId);
                    pst.executeUpdate();
                }
            }
            
            JOptionPane.showMessageDialog(this, "User updated successfully!");
            loadUsersTable();
            clearFields();
            setFieldsEnabled(false);
            setupButtons();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating user: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

