    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (!txtUsername.isEnabled()) {
            setFieldsEnabled(true);
            btnReset.setEnabled(true);
            btnAdd.setEnabled(true);
            clearFields();
            return;
        }
        
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText();
        String confirmPassword = txtConfirmPassword.getText();  
        String fullName = txtFullName.getText().trim();
        String role = cmbRole.getSelectedItem().toString();
        
        // Validation
        if (username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty() || fullName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields!");
            return;
        }
        
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!");
            txtConfirmPassword.setText("");
            txtConfirmPassword.requestFocus();
            return;
        }
        
        if (isUsernameExists(username, 0)) {
            JOptionPane.showMessageDialog(this, "Username '" + username + "' already exists!");
            return;
        }
        
        String sql = "INSERT INTO users (username, password, role, full_name) VALUES (?, MD5(?), ?, ?)";
        try (Connection con = ConnectorXampp.connect();
             PreparedStatement pst = con.prepareStatement(sql)) {
            
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, role);
            pst.setString(4, fullName);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "User '" + username + "' added successfully!");
            loadUsersTable();
            clearFields();
            setFieldsEnabled(false);
            setupButtons();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error adding user: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user to edit!");
            return;
        }
        
        editingUserId = (Integer) jTable1.getValueAt(selectedRow, 0);
        txtUsername.setText((String) jTable1.getValueAt(selectedRow, 1));
        cmbRole.setSelectedItem(jTable1.getValueAt(selectedRow, 2));
        txtFullName.setText((String) jTable1.getValueAt(selectedRow, 3));
        
        txtPassword.setText("");           
        txtConfirmPassword.setText("");   
        
        setFieldsEnabled(true);
        btnAdd.setEnabled(false);
        btnEdit.setEnabled(false);
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user to delete!");
            return;
        }
        
        String username = (String) jTable1.getValueAt(selectedRow, 1);
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Delete user '" + username + "'?\nThis cannot be undone!", 
            "Confirm Delete", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            int userId = (Integer) jTable1.getValueAt(selectedRow, 0);
            String sql = "DELETE FROM users WHERE id = ?";
            
            try (Connection con = ConnectorXampp.connect();
                 PreparedStatement pst = con.prepareStatement(sql)) {
                pst.setInt(1, userId);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "User deleted successfully!");
                loadUsersTable();
                clearFields();
                setupButtons();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error deleting user: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

